package org.kuali.common.util;

public class PropertyLocation {

	boolean gav;
	String location;
	public boolean isGav() {
		return gav;
	}
	public void setGav(boolean gav) {
		this.gav = gav;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}

}
