package org.kuali.common.util.property.processor;

public class AppConfig {

	String orgCode;
	String orgHome;
	String orgPropertiesLocation;
	String groupCode;
	String groupHome;
	String groupPropertiesLocation;
	String appCode;
	String appHome;
	String appPropertiesLocation;

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public String getOrgHome() {
		return orgHome;
	}

	public void setOrgHome(String orgHome) {
		this.orgHome = orgHome;
	}

	public String getGroupCode() {
		return groupCode;
	}

	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}

	public String getGroupHome() {
		return groupHome;
	}

	public void setGroupHome(String groupHome) {
		this.groupHome = groupHome;
	}

	public String getAppCode() {
		return appCode;
	}

	public void setAppCode(String appCode) {
		this.appCode = appCode;
	}

	public String getAppHome() {
		return appHome;
	}

	public void setAppHome(String appHome) {
		this.appHome = appHome;
	}

	public String getAppPropertiesLocation() {
		return appPropertiesLocation;
	}

	public void setAppPropertiesLocation(String appProperties) {
		this.appPropertiesLocation = appProperties;
	}

	public String getOrgPropertiesLocation() {
		return orgPropertiesLocation;
	}

	public void setOrgPropertiesLocation(String orgPropertiesLocation) {
		this.orgPropertiesLocation = orgPropertiesLocation;
	}

	public String getGroupPropertiesLocation() {
		return groupPropertiesLocation;
	}

	public void setGroupPropertiesLocation(String groupPropertiesLocation) {
		this.groupPropertiesLocation = groupPropertiesLocation;
	}

}
